package ie.atu;

public class Passenger {
    String userTitle;
    String userName;
    String userID;
    String userPhoneNum;
    int userAge;

    public Passenger(String title, String name, String id, String phoneNum, int age){
        setTitle(title);
        setName(name);
        setUserID(id);
        setPhoneNumber(phoneNum);
        setAge(age);
    }
}
